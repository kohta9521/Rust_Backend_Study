fn main() {
    println!("2 + 3 = {}", 2 + 3);
    println!("5 - 2 = {}", 5 - 2);
    println!("3 * 4 = {}", 3 * 4);
    println!("5 + -1 = {}", 5 + -1);
    // ちょっと難しめ
    println!("14を3で割ると、商が{}であまりが{}です。", 14 / 3, 14 * 3);

    // 浮動小数点
    println!("{}", 6.5 / 2.5);
    println!("{}", 6.5 % 2.5);
}